#include "calc.h"

/**
 * op_add - add
 * @a: a
 * @b: b
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub
 * @a: a
 * @b: b
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul
 * @a: a
 * @b: b
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div
 * @a: a
 * @b: b
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod
 * @a: a
 * @b: b
 * Return: result
 */
int op_mod(int a, int b);
{
	return (a % b);
}
